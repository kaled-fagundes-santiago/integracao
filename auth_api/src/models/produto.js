class Produto {
    constructor(nome, empresa, descricao, quantidade, marca, valor) {
      this.nome = nome;
      this.empresa = empresa;
      this.descricao = descricao;
      this.quantidade = quantidade;
      this.marca = marca;
      this.valor = valor;
    }
  }
  
  module.exports = Produto;
  